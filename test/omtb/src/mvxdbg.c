MX_Result XdbgPutS(char *str)
{
  fputs(str, stdout);
  return ((MX_Result)ERR_NOERR);
}


/*******************************************************************************
* FUNCTION:                                                
********************************************************************************
* DESCRIPTION:      
********************************************************************************
* RETURNS:                                                                      
*******************************************************************************/
MX_Result XdbgPrintf(const char *fmt, ...)
{
  va_list         args;
  
  va_start(args, fmt);
  vprintf(fmt, args);
  va_end(args);
  
  return ((MX_Result)ERR_NOERR);
}


/*******************************************************************************
* FUNCTION:                                                
********************************************************************************
* DESCRIPTION:      
********************************************************************************
* RETURNS:                                                                      
*******************************************************************************/
int Xsprintf(char *str, char* fmt, ...)
{
  int             ret;
  va_list         args;
  
  va_start(args, fmt);
  ret = vsprintf(str, fmt, args);
  va_end(args);
  
  return ret;
}

